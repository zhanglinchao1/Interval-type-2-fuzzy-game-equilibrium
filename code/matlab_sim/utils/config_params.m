function params = config_params()
%CONFIG_PARAMS 返回仿真所需的全部参数结构体
%   严格对应论文 chapter5.md §5.1 表5.1 参数设置，并满足 chapter4.md 公式约束:
%     (3-8) θ = P_pay · ω
%     (3-14)(4-2) δ ∈ [0, 1] 不确定性半带宽
%     (4-15) α ∈ (0, 1] 鲁棒 α-FNE 切割
%     (4-21) β ∈ (0, 1] 阻尼步长
%     (4-38) ε_g 治理慢时标步长
%
%   所有"中位值"取自论文扫描区间的中间值，便于单次默认调用复现核心实验

    % ===== 1. 复现性 =====
    params.rng_seed = 42;

    % ===== 2. 智能体与策略集 (论文 §3.1.1, 表5.1) =====
    params.N = 50;                         % 智能体数量 (扫描 {20,50,100} 的中位值)
    params.S = {'SC','SP','DC','DP'};      % 论文 (3-2): SC=安全合作, SP=安全自利,
                                            %             DC=非安全合作, DP=非安全自利
    params.num_strategies = 4;

    % ===== 3. 区块链治理参数 (论文 §3.2, §4.4) =====
    params.K = 5;                          % 治理规则数量 (论文未指定; 默认 5 条规则)

    % P_pay (3×K): 治理规则到收益层权重的映射矩阵，论文 (3-8) 要求列归一化
    %   设计依据: 让 P_pay × uniform_K = [0.40; 0.35; 0.25]，
    %             反映论文 "可信(40%) > 时效(35%) > 资源(25%)" 的优先级
    %   每列对应一条治理规则的 (trust, delay, res) 权重，列和 = 1
    params.P_pay = [0.50, 0.45, 0.40, 0.30, 0.35;
                    0.30, 0.40, 0.35, 0.40, 0.30;
                    0.20, 0.15, 0.25, 0.30, 0.35];

    % 治理权重初始值 (实验三初值): K 维均匀分布
    params.omega_init = ones(params.K, 1) / params.K;

    % 收益层权重 θ 由论文 (3-8) 直接计算: θ = P_pay × ω
    %   验证: 当 ω=uniform_K 时，θ = [0.40; 0.35; 0.25]
    params.theta = params.P_pay * params.omega_init;

    % 治理慢时标步长 ε_g (论文 (4-38b))
    %   扫描 {1e-4, 1e-3, 1e-2} 的中位值
    params.varepsilon_g = 1e-3;

    % ===== 4. 不确定性参数 (论文 §3.3, §4.1) =====
    params.delta = 0.10;                   % IT2 半带宽 (扫描 {0, 0.05, 0.10, 0.20} 中位值)
    params.alpha = 0.5;                    % α-cut 鲁棒切割 (扫描 {0.3, 0.5, 0.8, 1.0} 中位值)

    % ===== 5. W-FBRI 算法参数 (论文 §4.3.1) =====
    params.lambda = 0.10;                  % 熵正则系数 (扫描 {0.05, 0.10, 0.20} 中位值)
    params.beta = 0.3;                     % 阻尼步长 (扫描 {0.1, 0.3, 0.5} 中位值)
    params.R_max = 1000;                   % 最大迭代轮数 (论文表5.1)
    params.eps_tol = 1e-4;                 % 收敛阈值 (论文表5.1)

    % ===== 6. 双时间尺度演化参数 (论文 §4.4, (4-38)) =====
    %   T_evo × dt_evo = 100 (演化总时长), 让 ε_g × T × dt 跨 1e-2~1 量级,
    %   保证不同 ε_g 下 ω 演化幅度有显著差异 (实验三需求)
    params.T_evo = 2000;
    params.dt_evo = 0.05;

    % ===== 7. 状态隶属度生成矩阵 (合成测试用，对应论文 §3.3 M^(k)) =====
    % 含义: M^(k)(i,j) = 当本智能体策略=S_i、邻居/群体策略=S_j 时对状态 k 的隶属度贡献
    % 论文未给具体数值，下列为合成测试矩阵
    %   - trust: 协作维度，SC 与 SC 协作高、SP/DC 中等、DP 低
    %   - delay: 时效维度，安全协议(SC,SP)更可靠
    %   - res:   资源维度，与策略意愿和质量挂钩
    % 行: 本智能体策略 {SC, SP, DC, DP}
    % 列: 群体/邻居策略 {SC, SP, DC, DP}
    params.trust_matrix = [0.9, 0.7, 0.3, 0.1;
                           0.8, 0.6, 0.25, 0.1;
                           0.5, 0.4, 0.2, 0.1;
                           0.3, 0.2, 0.15, 0.05];

    params.delay_matrix = [0.85, 0.75, 0.5, 0.3;
                           0.8, 0.7, 0.45, 0.25;
                           0.6, 0.5, 0.35, 0.2;
                           0.4, 0.3, 0.2, 0.1];

    params.res_matrix = [0.7, 0.8, 0.6, 0.4;
                         0.65, 0.85, 0.55, 0.35;
                         0.75, 0.7, 0.5, 0.3;
                         0.5, 0.6, 0.4, 0.2];
end
